n1 = float(input("Number 1: "))
n2 = float(input("Number 2: "))
o = input("Operation: ")

if o == '+':
    print(n1 + n2)
elif o == '-':
    print(n1 - n2)
elif o == '*':
    print(n1 * n2)
elif o == '/':
    print(n1 / n2)
elif o == '//':
    print(n1 // n2)
elif o == '**':
    print(n1 ** n2)
elif o == '%':
    print(n1 % n2)